/**
 * Splits this string use method split
 */

class WordCount {
  def count(text: String): Map[String, Int] =
    text
      .split(" ")
      .map(_.toLowerCase)
      .groupBy(x => x)
      .view.mapValues(_.length)
      .toMap
}
